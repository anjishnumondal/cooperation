<h4 style="color:#44546b"><strong>Co-operation &amp; Co-operative Society</strong></h4>

<p style="color:#44546b">Co-operation means working together jointly. A Co-operative Society is essentially an autonomous association of persons united voluntarily on the basis of equality to meet common economic, social and cultural needs and aspirations of the members through jointly owned and democratically controlled enterprises by honest means. Co-operative Societies are expected to function according to certain Co-operative Principles as well as per provisions of the Co-operative Societies Act and the Rules. Co-operatives are based on the values of self-help, self-responsibility, democracy, equality, equity and solidarity. In the tradition of their founders, co-operative members believe in the ethical values of honesty, openness, social responsibility and caring for others.</p>


<span style="color:#44546b">As per the latest revision made by International Co-operative Alliance in 1995, The seven Co-operative principles are:- </span><br>
&nbsp;<br>
<ul style="color:#44546b">
	<li>Voluntary and Open Membership</li>
	<li>Democratic Member Control</li>
	<li>Member Economic Participation</li>
	<li>Autonomy and Independence</li>
	<li>Education, Training and Information</li>
	<li>Co-operation among Co-operatives</li>
	<li>Concern for Community</li>
</ul>
