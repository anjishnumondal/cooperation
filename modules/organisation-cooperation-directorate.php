<h3>Future </h3>
<p>Vision and mission of the Co-operation Directorate is to percolate the benefits of the Co-operative Movement to the entire population of the State.</p>
 
<p>It is the policy of the Government to cover all mouzas in the entire rural area of the State by functioning Primary Agricultural Credit Co-operative Societies(PACS) through â€˜Re-organization/Revival of PACSâ€™ so as to provide agricultural credit and allied services at the doorstep of each and every farmers.</p>
 
 
<p>In the coming years, priority will be given towards creation of rural infrastructure through grass-root level co-operative societies for boosting up agricultural growth and value addition. Such infrastructure shall include Storage Godown, Cold Storages, Seed Villages with Seed Grading Units, Custom Hiring Centre for farm mechanisation, Rural Haat, Processing Units, etc.</p>
 
<p>The Co-operation Directorate is committed to inculcate professionalism and skill development amongst the management and employees of the Co-operative Societies by way of Training for policy growth and good governance and at the same time awareness creation for increase in membership coverage as well as to encourage in business diversification.</p>
 
<p>Empowerment of women through formation of SHGs and Women Co-operatives is another thrust area in the days ahead.</p>
 
<p>Initiative has already been taken to construct â€˜Samabay Bhavanâ€™ in all Ranges to bring all offices related to Co-operation Department under one roof. Samabay Bhavan has already been completed in Burdwan-I, Burdwan-III, North 24-Pgs. and almost completed in Paschim Medinipur and Purulia Ranges. Construction work is going on at Bankura and Malda Ranges. Construction work will soon be started at Coochbehar and Dakshin Dinajpur Ranges.</p>
 
<p>Computerization of business operations of PACS for better management information system and service delivery and introduction of Deposit Guarantee Scheme to safeguard the interest of the small depositors is under active contemplation.</p>

