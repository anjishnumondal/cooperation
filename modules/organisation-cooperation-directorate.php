<h3>Introduction of the organization :</h3>
<p>Execution of the policies and decisions of the Co-operation Department is carried out through two Directorates, namely, Co-operation Directorate and Co-operative Audit Directorate. The Co-operation Directorate is headed by the Registrar of Co-operative Societies, West Bengal, who is a senior officer drawn from the IAS Cadre of the State.</p>
 
<p>The primary objective of the Co-operation Directorate is to see that genuine co-operatives are organized and managed on sound lines as per the basic principles of co-operation for the promotion of the economic interests and welfare of the people and to render guidance and assistance for the development of Co-operative movement in the State.</p>
 
<p>The Directorate has its Headquarters at the New Secretariat Buildings at 1, Kiran Sankar Roy Road, Kolkata - 700001 and the field operations are supervised and controlled by the Deputy/Assistant RCS of the Ranges with offices mainly at the District Head Quarters. The Range Offices are being assisted by the Cooperative Development Officers with assignment of Sub-Divisions and the Inspector of Cooperative Societies with assignment at Block and Municipal Circle level  providing the extension services in Development Blocks and Municipal areas spread all over the State. For decentralised supervision, administration and control there are at present three Zonal Offices of the Directorate at the Divisional Level headed by a Senior Officer in the rank of Joint RCS.</p>
 
<h3>Achievements</h3>
<p>The Co-operative Movement in India started more than 100 years ago in 1904 and started in West Bengal soon thereafter. Over the past few years the Co-operative Movement in our State has made significant progress.</p>
 
<p>At present there are more than 26000 registered co-operative societies having more than 76.00 lakh members. Total Storage capacity is around 7.00 lakh MT and total capacity of Cold Storages is about 3.50 lakh MT under co-operative fold. More than 21.00 lakh Kisan Credit Cards (KCC) have been issued and 1.88 lakh SHGs have been formed. During the year 2015-16 Crop Loan has been issued to the tune of Rs.3100.00 crore. Total deposit of Rs.20,759.48 crore has been mobilized under the co-operative sector as on 31-03-2015.</p>
 
<h3>Activities</h3>
<p>Co-operation Directorate acts as the friend-philosopher-guide to the Co-operative Movement in the State. Besides formation, supervision and monitoring of the different types of Co-operative Societies, it helps in nurturing the societies to achieve sustainable growth and self-sufficiency.</p>
 
<p>Co-operatives act as a socio-economic development tool particularly to the weaker section of the community and the Co-operation Directorate endeavours to promote the idea of Co-operative with an aim to bring more and more people under the umbrella of the Co-operative Movement so that the benefits permeate to the poorest of the poors.</p>
 
<p>Co-operation Directorate is constantly engaged in strengthening the Co-operative Societies and the Co-operative Movement as a whole by way of awareness creation, capacity building, providing management advice and hand-holding support, sharing of expertise.</p>
 
